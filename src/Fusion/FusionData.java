package Fusion;

public class FusionData {

	private double lon;
	
	private double lat;
	
	private int noise;
	
	private int light;

	public FusionData(double lon, double lat, int noise, int light) {
		super();
		this.lon = lon;
		this.lat = lat;
		this.noise = noise;
		this.light = light;
	}

	public double getLon() {
		return lon;
	}

	public void setLon(double lon) {
		this.lon = lon;
	}

	public double getLat() {
		return lat;
	}

	public void setLat(double lat) {
		this.lat = lat;
	}

	public int getNoise() {
		return noise;
	}

	public void setNoise(int noise) {
		this.noise = noise;
	}

	public int getLight() {
		return light;
	}

	public void setLight(int light) {
		this.light = light;
	}

	@Override
	public String toString() {
		return "FusionData [lon=" + lon + ", lat=" + lat + ", noise=" + noise
				+ ", light=" + light + "]";
	}
	
	
	

}
